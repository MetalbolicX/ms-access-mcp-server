class TestExtractSchema(_MigrationTestBase):
    """Tests for MigrationService.extract_schema()."""

    def test_extract_schema_returns_all_tables(self):
        """Full schema extraction includes customers, orders, products, type_test."""
        schema = self.service.extract_schema(self.adapter, FIXTURE_DB)
        table_names = [t.name for t in schema.tables]
        for name in ALL_TABLES:
            assert name in table_names

    def test_extract_schema_each_table_has_columns(self):
        """Each table has correct column names."""
        schema = self.service.extract_schema(self.adapter, FIXTURE_DB)
        table_by_name = {t.name: t for t in schema.tables}

        # customers table
        cust_cols = [c.name for c in table_by_name["customers"].columns]
        assert "ID" in cust_cols
        assert "Name" in cust_cols

        # orders table
        ord_cols = [c.name for c in table_by_name["orders"].columns]
        assert "ID" in ord_cols
        assert "CustomerID" in ord_cols
        assert "Total" in ord_cols

        # type_test table — all 9 column types
        type_cols = [c.name for c in table_by_name["type_test"].columns]
        for col in ("ID", "name", "active", "created", "price", "notes", "guid", "rating", "level"):
            assert col in type_cols

    @pytest.mark.skip(reason="Service does not populate primary_key yet")
    def test_extract_schema_primary_key_detected(self):
        """ID column is detected as primary key for extracted tables."""
        schema = self.service.extract_schema(self.adapter, FIXTURE_DB)
        table_by_name = {t.name: t for t in schema.tables}
        assert "ID" in table_by_name["customers"].primary_key
        assert "ID" in table_by_name["type_test"].primary_key

    def test_extract_schema_idempotent(self):
        """Calling extract_schema twice returns equivalent results."""
        schema1 = self.service.extract_schema(self.adapter, FIXTURE_DB)
        schema2 = self.service.extract_schema(self.adapter, FIXTURE_DB)

        names1 = sorted(t.name for t in schema1.tables)
        names2 = sorted(t.name for t in schema2.tables)
        assert names1 == names2

        for t1, t2 in zip(schema1.tables, schema2.tables):
            cols1 = sorted(c.name for c in t1.columns)
            cols2 = sorted(c.name for c in t2.columns)
            assert cols1 == cols2


