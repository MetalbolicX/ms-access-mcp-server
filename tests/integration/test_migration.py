class TestTransferData(_MigrationTestBase):
    """Tests for MigrationService.transfer_data() with SQLite target."""

    def setup_method(self):
        super().setup_method()
        self.schema = self.service.extract_schema(self.adapter, FIXTURE_DB)

    def test_transfer_row_counts_match_access(self, tmp_path):
        """SQLite row counts match Access row counts for all tables."""
        sqlite_path, _ = _run_full_pipeline(self.service, self.adapter, tmp_path)

        access_counts = _get_access_counts(self.adapter)
        sqlite_counts = _get_sqlite_counts(sqlite_path)

        for table in ALL_TABLES:
            assert sqlite_counts[table] == access_counts[table], (
                f"{table}: SQLite has {sqlite_counts[table]}, Access has {access_counts[table]}"
            )

    def test_transfer_values_preserved(self, tmp_path):
        """Representative rows preserve NULLs, booleans, currency, datetime, GUIDs, numeric values."""
        sqlite_path, _ = _run_full_pipeline(self.service, self.adapter, tmp_path)

        conn = sqlite3.connect(sqlite_path)
        cur = conn.cursor()
        cur.execute('SELECT name, active, created, price, notes, guid, rating, level FROM "type_test" ORDER BY ID')
        rows = cur.fetchall()
        conn.close()

        assert len(rows) >= 1, "type_test should have at least 1 row after transfer"

        for row in rows:
            name, active, created, price, notes, guid, rating, level = row
            if name is not None:
                assert isinstance(name, str)
            if active is not None:
                assert active in (0, 1), f"Expected boolean int, got {type(active).__name__}={active}"
            if created is not None:
                assert isinstance(created, str) and len(created) > 0
            if price is not None:
                assert isinstance(price, float)
            if notes is not None:
                assert isinstance(notes, str)
            if guid is not None:
                assert isinstance(guid, str) and len(guid) > 0
            if rating is not None:
                assert isinstance(rating, float)
            if level is not None:
                assert isinstance(level, int)

    def test_get_job_status_completed(self, tmp_path):
        """Completed job has status='completed', progress=1.0, and per-table results."""
        sqlite_path, job_id = _run_full_pipeline(self.service, self.adapter, tmp_path)

        status = self.service.get_job_status(job_id)
        assert status["success"] is True
        job = status["job"]

        assert job["status"] == "completed"
        assert job["progress"] == 1.0
        assert len(job["results"]) == 4

        result_tables = {r["table"] for r in job["results"]}
        assert result_tables == set(ALL_TABLES)

        for r in job["results"]:
            assert r["success"] is True, f"Table {r['table']} failed: {r.get('error')}"

    def test_extract_schema_disconnected_adapter_returns_empty(self):
        """Calling extract_schema with disconnected adapter returns empty schema (no error raised)."""
        from ms_access_mcp.adapters.wincom import WinComAdapter
        from ms_access_mcp.services.migration import MigrationService

        disconnected_adapter = WinComAdapter()
        assert not disconnected_adapter.is_connected()
        service = MigrationService()

        schema = service.extract_schema(disconnected_adapter, FIXTURE_DB)
        assert len(schema.tables) == 0

    def test_upload_schema_invalid_path(self):
        """upload_schema with non-existent parent directory returns success=False."""
        invalid_path = "Z:/nonexistent/path/that/does/not/exist/maybe/no_perms/test.db"
        result = self.service.upload_schema("sqlite", invalid_path, self.schema)
        assert result["success"] is False
        assert "tables_created" in result
        assert "tables_failed" in result

    def test_transfer_data_invalid_path(self):
        """transfer_data with non-existent directory returns success=False and connector error."""
        invalid_path = "Z:/nonexistent/path/that/does/not/exist/maybe/no_perms/test.db"
        result = self.service.transfer_data("sqlite", invalid_path, self.schema, self.adapter)
        assert result["success"] is False
        assert "job_id" in result
        assert "error" in result