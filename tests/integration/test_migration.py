class TestUploadSchema(_MigrationTestBase):
    """Tests for MigrationService.upload_schema() with SQLite target."""

    def setup_method(self):
        super().setup_method()
        self.schema = self.service.extract_schema(self.adapter, FIXTURE_DB)

    def test_upload_creates_sqlite_tables(self, temp_sqlite_db):
        """Upload creates all expected tables in SQLite DB."""
        result = self.service.upload_schema("sqlite", temp_sqlite_db, self.schema)
        assert result["success"] is True
        _assert_sqlite_tables_exist(temp_sqlite_db)

    def test_upload_column_types_map_correctly(self, temp_sqlite_db):
        """Column types map correctly: Boolean→INTEGER, Date/Time→TEXT, Currency→REAL, Memo→TEXT, GUID→TEXT."""
        self.service.upload_schema("sqlite", temp_sqlite_db, self.schema)

        conn = sqlite3.connect(temp_sqlite_db)
        cur = conn.cursor()
        cur.execute(f'PRAGMA table_info("type_test")')
        columns = {row[1]: row[2] for row in cur.fetchall()}
        conn.close()

        # ID — Long Integer → INTEGER
        assert columns["ID"] == "INTEGER"
        # name — Text → TEXT
        assert columns["name"] == "TEXT"
        # active — Boolean → INTEGER
        assert columns["active"] == "INTEGER"
        # created — Date/Time → TEXT
        assert columns["created"] == "TEXT"
        # price — Currency → REAL
        assert columns["price"] == "REAL"
        # notes — Memo → TEXT
        assert columns["notes"] == "TEXT"
        # guid — GUID → TEXT
        assert columns["guid"] == "TEXT"
        # rating — Double → REAL
        assert columns["rating"] == "REAL"
        # level — Byte → INTEGER
        assert columns["level"] == "INTEGER"

    def test_upload_existing_table_reported_in_failed(self, temp_sqlite_db):
        """Upload with already-existing table reports it in tables_failed (no duplicates)."""
        # First upload — should succeed
        result1 = self.service.upload_schema("sqlite", temp_sqlite_db, self.schema)
        assert result1["success"] is True
        tables_created = set(result1["tables_created"])

        # Second upload — existing tables should be in tables_failed
        result2 = self.service.upload_schema("sqlite", temp_sqlite_db, self.schema)
        assert result2["success"] is True
        assert len(result2["tables_created"]) == 0
        assert set(result2["tables_failed"]) == tables_created


