def _get_sqlite_tables(sqlite_path: str) -> list[str]:
    """Return sorted table names in an SQLite database."""
    conn = sqlite3.connect(sqlite_path)
    cur = conn.cursor()
    cur.execute("SELECT name FROM sqlite_master WHERE type='table' ORDER BY name")
    names = [row[0] for row in cur.fetchall()]
    conn.close()
    return names


def _get_sqlite_counts(sqlite_path: str) -> dict[str, int]:
    """Return {table: row_count} for ALL_TABLES from an SQLite database."""
    conn = sqlite3.connect(sqlite_path)
    cur = conn.cursor()
    counts = {}
    for table in ALL_TABLES:
        cur.execute(f'SELECT COUNT(*) FROM "{table}"')
        counts[table] = cur.fetchone()[0]
    conn.close()
    return counts


def _get_access_counts(adapter) -> dict[str, int]:
    """Return {table: row_count} for ALL_TABLES from Access via adapter."""
    counts = {}
    for table in ALL_TABLES:
        result = adapter.execute_query(f"SELECT COUNT(*) AS cnt FROM [{table}]")
        assert result["success"] is True, f"Query failed for {table}: {result.get('error')}"
        rows = result["rows"]
        counts[table] = rows[0]["cnt"] if rows else 0
    return counts


def _assert_sqlite_tables_exist(sqlite_path: str, expected: list[str] | None = None) -> None:
    """Assert that all expected tables exist in the SQLite database."""
    expected = expected or ALL_TABLES
    table_names = _get_sqlite_tables(sqlite_path)
    for name in expected:
        assert name in table_names, f"Table '{name}' missing from SQLite"


def _run_full_pipeline(service, adapter, tmp_path) -> tuple[str, str]:
    """Run extract → upload → transfer pipeline, return (sqlite_path, job_id)."""
    schema = service.extract_schema(adapter, FIXTURE_DB)
    sqlite_path = str(tmp_path / "transfer_target.db")
    upload_result = service.upload_schema("sqlite", sqlite_path, schema)
    assert upload_result["success"] is True, f"Schema upload failed: {upload_result}"
    result = service.transfer_data("sqlite", sqlite_path, schema, adapter)
    assert result["success"] is True, f"Transfer failed: {result.get('error', 'unknown')}"
    return sqlite_path, result["job_id"]


