class _MigrationTestBase:
    """Shared setup/teardown for all migration test classes.

    Each class creates its own WinComAdapter + MigrationService pair to avoid
    carrying connection state across class boundaries.
    """

    def setup_method(self):
        from ms_access_mcp.adapters.wincom import WinComAdapter
        from ms_access_mcp.services.migration import MigrationService

        self.adapter = WinComAdapter()
        assert self.adapter.connect(FIXTURE_DB), f"Failed to connect to {FIXTURE_DB}"
        self.service = MigrationService()

    def teardown_method(self):
        self.adapter.disconnect()


