"""Abstract adapter interfaces for MS Access operations.

The old God Protocol (AccessAdapter) has been split into focused interfaces
(PR2 ISP split):
- IConnectionManager: minimal lifecycle (connect/disconnect/is_connected)
- IDataAdapter: data CRUD and query execution
- ISchemaAdapter: schema introspection and DDL
- IFormAdapter: form/report lifecycle, properties, sections
- IVbaAdapter: VBA module read/write/compile
- IMacroAdapter: macro lifecycle
- IControlAdapter: form/report control manipulation
- IDatabasePropertiesAdapter: DAO CurrentDb.Properties
- IVersioningAdapter: database export/import/backup/restore

`IUiAdapter` is a TypeAlias combining the 6 focused UI protocols — kept for
backward compatibility with code that previously imported the monolithic
protocol.

WinComAdapter implements all interfaces.
OdbcAdapter implements IDataAdapter + ISchemaAdapter + IDatabasePropertiesAdapter
(form/VBA/macro/control/versioning methods raise NotImplementedError).

Backward compatibility: AccessAdapter remains a Protocol that defines
the full surface area of WinComAdapter (the original god interface).
"""

from typing import Any, Protocol, runtime_checkable

from .interfaces import (
    IControlAdapter,
    IDataAdapter,
    IDatabasePropertiesAdapter,
    IFormAdapter,
    IMacroAdapter,
    ISchemaAdapter,
    IUiAdapter,
    IVbaAdapter,
    IVersioningAdapter,
)


@runtime_checkable
class AccessAdapter(Protocol):
    """Abstract interface for MS Access operations — full surface area.

    .. deprecated::
        AccessAdapter is deprecated. Use the segregated protocols from
        `adapters.interfaces` instead — IDataAdapter, ISchemaAdapter,
        IFormAdapter, IVbaAdapter, IMacroAdapter, IControlAdapter,
        IDatabasePropertiesAdapter, IVersioningAdapter. The composite
        `IUiAdapter` TypeAlias still works for code that previously
        imported the monolithic UI protocol. AccessAdapter is retained
        for backward compatibility only.
    """

    def connect(self, db_path: str, password: str = "") -> bool: ...

    def disconnect(self) -> None: ...

    def is_connected(self) -> bool: ...

    def get_tables(self) -> list: ...

    def execute_query(self, sql: str, params: list | None = None) -> dict: ...

    def insert_data(self, table_name: str, data: dict | list[dict]) -> dict: ...

    def update_data(
        self, table_name: str, set_dict: dict, where_dict: dict | str | None = None
    ) -> dict: ...

    def delete_data(self, table_name: str, where_dict: dict | str | None = None) -> dict: ...

    def export_data(
        self, sql: str, file_path: str, format: str = "csv", **options: Any
    ) -> dict: ...

    def get_queries(self) -> list: ...

    def create_query(self, name: str, sql: str) -> dict: ...

    def set_query_sql(self, name: str, sql: str) -> dict: ...

    def delete_query(self, name: str) -> dict: ...

    def create_table(self, table_name: str, columns: list[dict]) -> dict: ...

    def delete_table(self, table_name: str) -> dict: ...

    def launch_access(self, visible: bool = False) -> None: ...

    def close_access(self) -> None: ...

    def set_vba_code(self, module_name: str, code: str) -> bool: ...

    def get_forms(self) -> list: ...

    def get_reports(self) -> list: ...

    def get_macros(self) -> list: ...

    def get_modules(self) -> list: ...

    def get_vba_code(self, module_name: str) -> str: ...

    def get_system_tables(self) -> list: ...

    def form_exists(self, form_name: str) -> bool: ...

    def report_exists(self, report_name: str) -> bool: ...

    def get_form_controls(self, form_name: str) -> list: ...

    def export_form_to_text(self, form_name: str) -> str: ...

    def import_form_from_text(self, form_name: str, form_data: str) -> bool: ...

    def delete_form(self, form_name: str) -> bool: ...

    def export_report_to_text(self, report_name: str) -> str: ...

    def import_report_from_text(self, report_name: str, report_data: str) -> bool: ...

    def delete_report(self, report_name: str) -> bool: ...

    def add_vba_procedure(self, module_name: str, procedure_name: str, code: str) -> bool: ...

    def compile_vba(self) -> dict: ...

    def save_database(self) -> dict: ...

    def open_form(self, form_name: str) -> bool: ...

    def close_form(self, form_name: str) -> bool: ...

    def get_control_properties(self, form_name: str, control_name: str) -> dict: ...

    def set_control_property(
        self, form_name: str, control_name: str, property_name: str, value: str
    ) -> bool: ...

    def set_control_properties(
        self, form_name: str, control_name: str, properties: dict[str, Any]
    ) -> dict[str, bool]: ...

    def create_form(
        self,
        form_name: str,
        record_source: str = "",
        template_name: str = "",
        properties: dict[str, Any] | None = None,
    ) -> bool: ...

    def rename_form(self, old_name: str, new_name: str) -> bool: ...

    def get_form_properties(self, form_name: str) -> dict: ...

    def set_form_property(self, form_name: str, property_name: str, value: str) -> bool: ...

    def set_form_properties(
        self, form_name: str, properties: dict[str, Any]
    ) -> dict[str, bool]: ...

    def get_form_sections(self, form_name: str) -> list: ...

    def get_form_section_properties(self, form_name: str, section_id: int) -> dict: ...

    def set_form_section_property(
        self, form_name: str, section_id: int, property_name: str, value: str
    ) -> bool: ...

    def set_form_section_properties(
        self, form_name: str, section_id: int, properties: dict[str, Any]
    ) -> dict[str, bool]: ...

    def add_control(
        self,
        form_name: str,
        control_type: str,
        control_name: str,
        section: int = 0,
        properties: dict[str, Any] | None = None,
    ) -> bool: ...

    def remove_control(self, form_name: str, control_name: str) -> bool: ...

    def get_control_event_procedures(self, form_name: str, control_name: str) -> list[dict]: ...

    def get_object_metadata(self, object_name: str) -> dict: ...

    def get_relationships(self) -> list: ...

    def generate_sql(self, output_path: str) -> dict: ...

    def execute_sql_script(self, script_path: str) -> dict: ...

    def export_module_to_text(self, module_name: str) -> str: ...

    def export_macro_to_text(self, macro_name: str) -> str: ...

    def macro_exists(self, macro_name: str) -> bool: ...

    def create_macro(self, macro_name: str) -> bool: ...

    def rename_macro(self, old_name: str, new_name: str) -> bool: ...

    def delete_macro(self, macro_name: str) -> bool: ...

    def run_macro(self, macro_name: str) -> bool: ...

    def get_macro_properties(self, macro_name: str) -> dict: ...

    def delete_module(self, module_name: str) -> bool: ...

    def export_all_versioning(self, output_dir: str) -> dict: ...

    def export_query_to_text(self, query_name: str) -> str: ...

    def import_query_from_text(self, query_name: str, query_data: str) -> bool: ...

    def import_all_versioning(self, input_dir: str) -> dict: ...

    def compare_versioning(self, export_dir: str) -> dict: ...

    def export_schema_ddl(self, output_dir: str) -> dict: ...

    def get_linked_tables(self) -> dict: ...

    def create_linked_table(self, name: str, source_table: str, connect_string: str) -> dict: ...

    def refresh_linked_table(self, name: str, connect_string: str | None = None) -> dict: ...

    def recreate_linked_table(
        self, name: str, source_table: str, connect_string: str, attributes: int | None = None
    ) -> dict: ...

    def unlink_table(self, name: str) -> dict: ...

    def compact_repair(
        self, action: str, source_path: str, dest_path: str, keep_original: bool = True
    ) -> dict: ...

    def copy_database(self, source: str, dest: str) -> bool: ...

    def alter_table(self, table_name: str, operations: list[dict]) -> dict: ...

    def vba_list_procedures(self, module_name: str) -> list[dict]: ...

    def vba_get_procedure(self, module_name: str, procedure_name: str) -> dict: ...

    def vba_replace_procedure(
        self, module_name: str, procedure_name: str, new_code: str
    ) -> bool: ...

    def set_control_event_procedure(
        self, form_name: str, control_name: str, event_name: str, code: str
    ) -> bool: ...

    def get_table_schema_plan(self) -> tuple: ...

    def execute_raw_sql(self, sql: str) -> int: ...

    def get_indexes(self, table_name: str) -> list: ...

    def create_index(
        self,
        table_name: str,
        index_name: str,
        columns: list[str],
        unique: bool = False,
        ignore_nulls: bool = False,
    ) -> dict: ...

    def drop_index(self, table_name: str, index_name: str) -> dict: ...


