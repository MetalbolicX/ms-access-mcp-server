@runtime_checkable
class IDatabasePropertiesAdapter(Protocol):
    """DAO CurrentDb.Properties read/write.

    WinComAdapter implements this via COM. OdbcAdapter declares it
    (raising NotImplementedError) so services can type-hint against
    IDatabasePropertiesAdapter uniformly.
    """

    def get_database_properties(self, names: list[str] | None = None) -> dict: ...
    def set_database_property(
        self, name: str, value: str, type: str | None = None
    ) -> bool: ...


@runtime_checkable
class IVersioningAdapter(Protocol):
    """Database versioning / backup / restore / export.

    WinComAdapter implements the full surface. OdbcAdapter does NOT
    implement this protocol — the methods are present as extra
    convenience methods only for AccessAdapter backward compatibility.
    """

    def export_all_versioning(self, output_dir: str) -> dict: ...
    def import_all_versioning(self, input_dir: str) -> dict: ...
    def compare_versioning(self, export_dir: str) -> dict: ...
    def export_schema_ddl(self, output_dir: str) -> dict: ...
    def export_query_to_text(self, query_name: str) -> str: ...
    def import_query_from_text(self, query_name: str, query_data: str) -> bool: ...
    def compact_repair(
        self, action: str, source_path: str, dest_path: str, keep_original: bool = True
    ) -> dict: ...
    def copy_database(self, source: str, dest: str) -> bool: ...
    # Spec-named versioning helpers (aliasing the canonical methods above)
    def export_database(self, output_dir: str) -> dict: ...
    def import_database(self, input_dir: str) -> dict: ...
    def create_backup(self, source_path: str, backup_path: str) -> bool: ...
    def restore_backup(self, backup_path: str, dest_path: str) -> bool: ...


# ========================================================================
# Backward-compat composite alias — IUiAdapter is the union of the 6
# focused UI protocols. Existing code that imported IUiAdapter keeps
# working; new code should import the focused protocol it actually needs.
# ========================================================================

IUiAdapter: TypeAlias = (
    IFormAdapter | IVbaAdapter | IMacroAdapter | IControlAdapter
    | IDatabasePropertiesAdapter | IVersioningAdapter
)
