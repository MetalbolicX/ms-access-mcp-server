"""Segregated adapter interfaces — ISP and LSP compliance.

WinComAdapter implements all interfaces. OdbcAdapter implements
IDataAdapter, ISchemaAdapter, and IDatabasePropertiesAdapter only.

The UI surface is split into 6 focused protocols (IFormAdapter, IVbaAdapter,
IMacroAdapter, IControlAdapter, IDatabasePropertiesAdapter, IVersioningAdapter).
`IUiAdapter` is retained as a `TypeAlias` combining all six for backward
compatibility with code that previously used the monolithic UI protocol.
"""

from typing import Any, Protocol, TypeAlias, runtime_checkable

from ..models.database import (
    ControlInfo,
    FormInfo,
    IndexInfo,
    MacroInfo,
    ModuleInfo,
    QueryInfo,
    RelationshipInfo,
    ReportInfo,
    TableInfo,
)
from ..models.migration import TableSchema, UnknownMetadata


@runtime_checkable
class IConnectionManager(Protocol):
    """Minimal lifecycle — connect, disconnect, is_connected."""

    def connect(self, db_path: str, password: str = "") -> bool: ...
    def disconnect(self) -> None: ...
    def is_connected(self) -> bool: ...


@runtime_checkable
class IDataAdapter(IConnectionManager, Protocol):
    """Data CRUD and query execution — ODBC and COM both support this."""

    def execute_query(self, sql: str, params: list | None = None) -> dict: ...
    def insert_data(self, table_name: str, data: dict | list[dict]) -> dict: ...
    def update_data(
        self, table_name: str, set_dict: dict, where_dict: dict | str | None = None
    ) -> dict: ...
    def delete_data(self, table_name: str, where_dict: dict | str | None = None) -> dict: ...
    def export_data(
        self, sql: str, file_path: str, format: str = "csv", **options: Any
    ) -> dict: ...
    def execute_raw_sql(self, sql: str) -> int: ...


@runtime_checkable
class ISchemaAdapter(IConnectionManager, Protocol):
    """Schema introspection and DDL — ODBC and COM both support this."""

    def get_tables(self) -> list[TableInfo]: ...
    def get_system_tables(self) -> list[TableInfo]: ...
    def get_object_metadata(self, object_name: str) -> dict: ...
    def get_relationships(self) -> list[RelationshipInfo]: ...
    def get_table_schema_plan(self) -> tuple[list[TableSchema], UnknownMetadata]: ...
    def generate_sql(self, output_path: str) -> dict: ...
    def get_database_statistics(self) -> dict: ...
    def get_queries(self) -> list[QueryInfo]: ...
    def create_query(self, name: str, sql: str) -> dict: ...
    def set_query_sql(self, name: str, sql: str) -> dict: ...
    def delete_query(self, name: str) -> dict: ...
    def create_table(self, table_name: str, columns: list[dict]) -> dict: ...
    def delete_table(self, table_name: str) -> dict: ...
    def get_linked_tables(self) -> dict: ...
    def create_linked_table(self, name: str, source_table: str, connect_string: str) -> dict: ...
    def refresh_linked_table(self, name: str, connect_string: str | None = None) -> dict: ...
    def recreate_linked_table(
        self, name: str, source_table: str, connect_string: str, attributes: int | None = None
    ) -> dict: ...
    def unlink_table(self, name: str) -> dict: ...
    def execute_sql_script(self, script_path: str) -> dict: ...
    def alter_table(self, table_name: str, operations: list[dict]) -> dict: ...
    def get_indexes(self, table_name: str) -> list[IndexInfo]: ...
    def create_index(
        self,
        table_name: str,
        index_name: str,
        columns: list[str],
        unique: bool = False,
        ignore_nulls: bool = False,
    ) -> dict: ...
    def drop_index(self, table_name: str, index_name: str) -> dict: ...
    def create_relationship(
        self,
        table_name: str,
        relationship_name: str,
        columns: list[str],
        foreign_table: str,
        foreign_columns: list[str],
    ) -> dict: ...
    def delete_relationship(self, table_name: str, relationship_name: str) -> dict: ...


# ========================================================================
# Segregated UI protocols (Interface Segregation Principle split)
# Each Protocol is small and focused. WinComAdapter implements all six.
# OdbcAdapter implements IDatabasePropertiesAdapter (with NotImplementedError
# stubs) and the rest of the UI surface is provided by ComOnlyAdapterMixin.
# ========================================================================


@runtime_checkable
class IFormAdapter(Protocol):
    """Form-level operations — open/close, controls, properties, sections.

    Backed by Access COM automation. ODBC cannot read form definitions.
    """

    def get_forms(self) -> list[FormInfo]: ...
    def form_exists(self, form_name: str) -> bool: ...
    def create_form(
        self,
        form_name: str,
        record_source: str = "",
        template_name: str = "",
        properties: dict[str, Any] | None = None,
    ) -> bool: ...
    def rename_form(self, old_name: str, new_name: str) -> bool: ...
    def delete_form(self, form_name: str) -> bool: ...
    def open_form(self, form_name: str) -> bool: ...
    def close_form(self, form_name: str) -> bool: ...
    def get_form_properties(self, form_name: str) -> dict: ...
    def set_form_property(self, form_name: str, property_name: str, value: str) -> bool: ...
    def set_form_properties(
        self, form_name: str, properties: dict[str, Any]
    ) -> dict[str, bool]: ...
    def get_form_sections(self, form_name: str) -> list: ...
    def get_form_section_properties(self, form_name: str, section_id: int) -> dict: ...
    def set_form_section_property(
        self, form_name: str, section_id: int, property_name: str, value: str
    ) -> bool: ...
    def set_form_section_properties(
        self, form_name: str, section_id: int, properties: dict[str, Any]
    ) -> dict[str, bool]: ...
    # Reports (grouped with forms because they share lifecycle and property model)
    def get_reports(self) -> list[ReportInfo]: ...
    def report_exists(self, report_name: str) -> bool: ...
    def create_report(
        self,
        report_name: str,
        record_source: str = "",
        template_name: str = "",
        properties: dict[str, Any] | None = None,
    ) -> bool: ...
    def rename_report(self, old_name: str, new_name: str) -> bool: ...
    def delete_report(self, report_name: str) -> bool: ...
    def get_report_properties(self, report_name: str) -> dict: ...
    def set_report_property(self, report_name: str, property_name: str, value: str) -> bool: ...
    def set_report_properties(
        self, report_name: str, properties: dict[str, Any]
    ) -> dict[str, bool]: ...
    def get_report_sections(self, report_name: str) -> list: ...
    def get_report_section_properties(self, report_name: str, section_id: int) -> dict: ...
    def set_report_section_property(
        self, report_name: str, section_id: int, property_name: str, value: str
    ) -> bool: ...
    def set_report_section_properties(
        self, report_name: str, section_id: int, properties: dict[str, Any]
    ) -> dict[str, bool]: ...
    # Form / report text export-import pipeline
    def export_form_to_text(self, form_name: str) -> str: ...
    def import_form_from_text(self, form_name: str, form_data: str) -> bool: ...
    def export_report_to_text(self, report_name: str) -> str: ...
    def import_report_from_text(self, report_name: str, report_data: str) -> bool: ...
    # Spec-named form helpers
    def get_form_controls(self, form_name: str) -> list[ControlInfo]: ...
    def export_form_to_html(self, form_name: str) -> str: ...
    def get_control_property(
        self, form_name: str, control_name: str, property_name: str
    ) -> Any: ...
    def set_control_property(
        self, form_name: str, control_name: str, property_name: str, value: str
    ) -> bool: ...


